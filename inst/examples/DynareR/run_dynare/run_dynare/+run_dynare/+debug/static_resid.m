function [lhs, rhs] = static_resid(y, x, params)
T = NaN(4, 1);
lhs = NaN(6, 1);
rhs = NaN(6, 1);
T(1) = y(5)^(1+params(6));
T(2) = exp(y(4))*y(3)^params(3);
T(3) = y(5)^(1-params(3));
T(4) = T(2)*T(3);
lhs(1) = y(2)*params(5)*T(1);
rhs(1) = (1-params(3))*y(1);
lhs(2) = y(3);
rhs(2) = params(1)*(y(1)*params(3)*exp(y(6))+y(3)*(1-params(4)));
lhs(3) = y(1);
rhs(3) = T(4);
lhs(4) = y(3);
rhs(4) = y(3)*(1-params(4))+exp(y(6))*(y(1)-y(2));
lhs(5) = y(4);
rhs(5) = y(4)*params(2)+y(6)*params(7)+x(1);
lhs(6) = y(6);
rhs(6) = y(4)*params(7)+y(6)*params(2)+x(2);
end
