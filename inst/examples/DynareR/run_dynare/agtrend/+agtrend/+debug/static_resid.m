function [lhs, rhs] = static_resid(y, x, params)
T = NaN(10, 1);
lhs = NaN(15, 1);
rhs = NaN(15, 1);
T(1) = exp(y(9))*y(2)^(1-params(9));
T(2) = (y(6)*y(7))^params(9);
T(3) = y(1)-params(14)*y(7)^params(15);
T(4) = T(3)^(-params(2));
T(5) = params(15)*(-params(14))*y(7)^(params(15)-1);
T(6) = params(6)/2;
T(7) = T(6)*(y(6)-params(1))^2;
T(8) = y(2)*T(7);
T(9) = 1-params(5)+y(3)*(1-params(9))/y(2)+T(6)*(y(6)-params(1))*(y(6)+params(1));
T(10) = (T(8)+y(2)*y(6)-y(2)*(1-params(5)))/y(3);
lhs(1) = y(3);
rhs(1) = T(1)*T(2);
lhs(2) = y(9);
rhs(2) = y(9)*params(10)+params(11)*x(1);
lhs(3) = log(y(6));
rhs(3) = (1-params(3))*log(params(1))+log(y(6))*params(3)+params(4)*x(2);
lhs(4) = y(8);
rhs(4) = T(3)^(1-params(2))/(1-params(2));
lhs(5) = y(10);
rhs(5) = T(4);
lhs(6) = y(11);
rhs(6) = T(4)*T(5);
lhs(7) = y(12);
rhs(7) = params(13)*y(6)^(1-params(2));
lhs(8) = y(1)+y(2)*y(6);
rhs(8) = y(3)+y(2)*(1-params(5))-T(8)-y(4)+y(4)*y(6)*y(5);
lhs(9) = 1/y(5);
rhs(9) = 1+params(12)+params(7)*(exp(y(4)-params(8))-1);
lhs(10) = y(6)*y(10)*(1+params(6)*(y(6)-params(1)));
rhs(10) = y(10)*y(12)*T(9);
lhs(11) = y(11)+y(3)*params(9)*y(10)/y(7);
rhs(11) = 0;
lhs(12) = y(5)*y(6)*y(10);
rhs(12) = y(10)*y(12);
lhs(13) = y(14);
rhs(13) = (y(4)-y(4)*y(6)*y(5))/y(3);
lhs(14) = y(13);
rhs(14) = log(y(1)/y(3));
lhs(15) = y(15);
rhs(15) = log(T(10));
end
